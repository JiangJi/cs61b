
public class Planet {
    public Planet(double x,double y,double xv,double yv,double mass,String image)
    {
        this.x = x;
        this.y = y;
        this.xVelocity = xv;
        this.yVelocity = yv;
        this.mass = mass;
        this.img = image;
    }
    public double x;
    public double y;
    public double xVelocity;
    public double yVelocity;
    public double mass;
    public String img;
}
